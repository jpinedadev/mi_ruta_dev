//antes de ES6 la unica forma de declara una variable era usando var 
//ahora podemos usar var, let y const 

//si usas var fuera de una funcion esta va a ser global 
//si la usa dentro de una funcion sera local 
var x = 5.6;

//let es un bloque de alcance de la version de var y esta limitada 
//al bloque o expresion donde fue definida
let x = 5.6;

//const es una variable que una vez creada su valor no puede cambiar 
//no puedes: reasignar el valor, reasignar un array o reasignar un objeto 
//pero si puedes: cambiar los elementos del array y cambiar las propiedades de un objeto. 
const x = 5.6;


